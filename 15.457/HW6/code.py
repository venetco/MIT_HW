## QUESTION A

import numpy as np
import pandas as pd

df = pd.read_csv("Data_Pred_Return.csv")

if 'Unnamed: 0' in df.columns:
    df = df.drop(columns=['Unnamed: 0'])

df['dates'] = pd.to_datetime(df['dates'])
df = df.sort_values(['cusip', 'dates']).reset_index(drop=True)

# Log transforms of level variables
df['log_MV']    = np.where(df['MV']    > 0, np.log(df['MV']),    np.nan)
df['log_Price'] = np.where(df['Price'] > 0, np.log(df['Price']), np.nan)
df['log_Sales'] = np.where(df['Sales'] > 0, np.log(df['Sales']), np.nan)

# Cross-sectional demeaning
for col in ['log_MV', 'log_Price', 'log_Sales']: df[col + '_cs'] = df.groupby('dates')[col].transform(lambda x: x - x.mean())

# Sales growth
df['Sales_growth'] = df.groupby('cusip')['log_Sales'].diff()

# Ratios
df['leverage'] = df['SD2A'] + df['LD2A']
df['log_M2B'] = np.where(df['M2B'] > 0, np.log(df['M2B']), np.nan)
pe_low, pe_high = df['PE'].quantile([0.01, 0.99]) #cap extreme PE values to reduce outliers
df['PE_winsor'] = df['PE'].clip(lower=pe_low, upper=pe_high)

# Momentum
g = df.groupby('cusip', group_keys=False)
df['ret_1m']  = g['RET'].shift(1)
df['ret_3m']  = g['RET'].shift(1).rolling(3).sum()
df['ret_6m']  = g['RET'].shift(1).rolling(6).sum()
df['ret_12m'] = g['RET'].shift(1).rolling(12).sum()

# Final feature set
feature_cols = [
    'log_MV_cs',
    'log_Price_cs',
    'log_Sales_cs',
    'Sales_growth',
    'M2B',
    'log_M2B',
    'PE_winsor',
    'S2A',
    'SD2A',
    'LD2A',
    'leverage',
    'ret_1m',
    'ret_3m',
    'ret_6m',
    'ret_12m'
]

print(df[feature_cols].head())


