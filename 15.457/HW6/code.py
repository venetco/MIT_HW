from sklearn.metrics import confusion_matrix

def evaluate_model(model, X_test, y_test, name="model"):
    p_hat = model.predict_proba(X_test)[:, 1]
    y_pred = (p_hat >= 0.5).astype(int)

    cm = confusion_matrix(y_test, y_pred)
    TN, FP, FN, TP = cm.ravel()

    type_I  = FP / (TN + FP) if (TN + FP) > 0 else np.nan
    type_II = FN / (TP + FN) if (TP + FN) > 0 else np.nan
    overall = (FP + FN) / (TP + TN + FP + FN)

    print(f"\n=== {name} ===")
    print("Confusion matrix:\n", cm)
    print(f"Type I error: {type_I:.4f}")
    print(f"Type II error: {type_II:.4f}")
    print(f"Overall error rate: {overall:.4f}")

    return {"confusion_matrix": cm,"type_I": type_I,"type_II": type_II,"overall": overall}

results_plain = evaluate_model(logit_model, X_test, y_test, "Plain Logistic Regression")
