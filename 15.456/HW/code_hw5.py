def convergence_call_vs_Np(S0, K, rf, T, sigma, Nt, Np_list):
    rows = []
    for npi in Np_list:
        mc = MCprice(S0, K, rf, T, sigma, Nt, npi, contract="call")
        rows.append({"Np": int(npi), "MC Price": mc["price"], "MC SE": mc["se"]})
    return pd.DataFrame(rows)


def plot_convergence(conv_df, bs_call):
    plt.figure()
    plt.semilogx(conv_df["Np"], conv_df["MC Price"], marker="o")
    plt.axhline(bs_call, linestyle="--")
    plt.xlabel("Number of paths (Np)")
    plt.ylabel("Call price")
    plt.title("Monte Carlo call price vs Np (semi-log x)")
    plt.tight_layout()
    plt.show()

    plt.figure()
    plt.semilogx(conv_df["Np"], conv_df["MC SE"], marker="o")
    plt.xlabel("Number of paths (Np)")
    plt.ylabel("Standard error")
    plt.title("Standard error vs Np (semi-log x)")
    plt.tight_layout()
    plt.show()


Np_list = [500, 1000, 2000, 4000, 8000, 16000, 32000, 64000, 128000, 256000]
conv_df = convergence_call_vs_Np(S0, K, rf, T, sigma, Nt, Np_list)
bs_call = analytical_values(S0, K, rf, T, sigma)["call"]
plot_convergence(conv_df, bs_call)