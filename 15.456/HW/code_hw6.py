# SPD and Arrow prices at terminal nodes n=0..N
def pi_T(n):
    return (beta**N) * (q/p)**n * ((1-q)/(1-p))**(N-n)

def arrow(n):
    return (beta**N) * comb(N, n) * (q**n) * ((1-q)**(N-n))

