# Wealth functions
def WT_single_from_ST(ST_over_S0, sigma, T, phi):
    return (ST_over_S0)**phi * np.exp(0.5 * sigma**2 * T * (phi - phi**2))

def WT_split_from_ST(ST_over_S0, sigma, T, phi):
    term_pos = 0.5 * WT_single_from_ST(ST_over_S0, sigma, T, +phi)
    term_neg = 0.5 * WT_single_from_ST(ST_over_S0, sigma, T, -phi)
    return term_pos + term_neg

