# Parameters
T = 24
S_BAR = 1.0
rho = 0.99
sigma = 0.01
lam = 0.01
X0 = 1.0


# Grids
# State 1: price deviation x_t = S_t - S_BAR
dx = sigma / 3.0
sigmass = np.sqrt(sigma**2 / (1 - rho**2))
xmax = 5 * sigmass
Nx = int(2 * np.ceil(xmax / dx) + 1)
xgrid = np.linspace(-xmax, xmax, Nx)
dx = xgrid[1] - xgrid[0]

# State 2: inventory W_t (shares left to sell)
Wmax = X0
NW = 81
Wgrid = np.linspace(0.0, Wmax, NW)
dW = Wgrid[1] - Wgrid[0]

# Control: number of shares to sell x_t
Ntheta = 81
thetagrid = np.linspace(0.0, Wmax, Ntheta)
dtheta = thetagrid[1] - thetagrid[0]


# Transition matrix for x_t
TrM_x = np.full((Nx, Nx), 0.0)

for i in range(Nx):
    mean = rho * xgrid[i]
    p = np.zeros(Nx)
    # interior points
    p[1:-1] = norm(mean, sigma).pdf(xgrid[1:-1]) * dx
    # first bin (left tail)
    p[0] = norm(mean, sigma).cdf(xgrid[0] + 0.5 * dx)
    # last bin (right tail)
    p[-1] = 1.0 - norm(mean, sigma).cdf(xgrid[-1] - 0.5 * dx)
    p = p / np.sum(p)
    TrM_x[i, :] = p


# Dynamic Programming
# J[t, ix, iw] = value function
# theta_opt[t, ix, iw] = optimal shares to sell at t

def build_dp():
    cur = time.time()

    J = np.full((T+1, Nx, NW), 0.0)
    theta_opt = np.full((T, Nx, NW), 0.0)

    # Terminal condition: at time T, value is zero (no more trading possible).
    # Constraint "W_T = 0" is enforced by forcing liquidation at t = T-1.

    # Backward induction
    for t in range(T-1, -1, -1):
        print(f"DP step t = {t}")
        for ix in range(Nx):
            x_prev = xgrid[ix]
            # Conditional expected execution price at time t:
            # E[S_t | x_{t-1}] = S_BAR + rho * x_prev
            m_t = S_BAR + rho * x_prev
            # probabilities of x_t given x_{t-1}=x_prev
            p_next = TrM_x[ix, :]

            for iw in range(NW):
                W = Wgrid[iw]

                # If no inventory, nothing to do
                if W <= 1e-12:
                    J[t, ix, iw] = 0.0
                    theta_opt[t, ix, iw] = 0.0
                    continue

                # Allowed controls: 0 <= theta <= W
                valid = thetagrid <= W
                theta_candidates = thetagrid[valid]
                if theta_candidates.size == 0:
                    J[t, ix, iw] = 0.0
                    theta_opt[t, ix, iw] = 0.0
                    continue

                # At final trading period t = T-1 we must liquidate everything:
                if t == T-1:
                    theta_candidates = np.array([W])

                values = []
                for theta in theta_candidates:
                    immediate = theta * (m_t - lam * theta)
                    W_next = W - theta
                    # snap to nearest inventory grid point
                    jW_next = int(np.argmin(np.abs(Wgrid - W_next)))

                    J_next_vec = J[t+1, :, jW_next]
                    EV = p_next.dot(J_next_vec)

                    values.append(immediate + EV)

                values = np.array(values)
                best_idx = int(np.argmax(values))
                J[t, ix, iw] = values[best_idx]
                theta_opt[t, ix, iw] = theta_candidates[best_idx]

    save("valueFunction.npy", J)
    save("optimalTheta.npy", theta_opt)

    now = time.time()
    print("DP finished in minutes:", (now - cur) / 60.0)


# Interpolation helper
def get_optimal_theta(theta_opt, t, S_prev, W):
    x_prev = S_prev - S_BAR
    ix = int(np.argmin(np.abs(xgrid - x_prev)))
    iw = int(np.argmin(np.abs(Wgrid - W)))
    return theta_opt[t, ix, iw]


