def analytic_two_period_x(W, S_prev):
    slope = rho * (1.0 - rho) / (4.0 * lam)
    x_star = W / 2.0 + slope * (S_prev - S_BAR)
    # clamp to [0, W]
    return np.clip(x_star, 0.0, W)

