import numpy as np
from sklearn.linear_model import LinearRegression
import matplotlib.pyplot as plt


# Parameters
Delta = 1.0 / 252.0
rF = 0.05
mu_S = 0.10
eta_x = -0.2
theta = 4.0
gamma_x = 2.0
rho = -0.9
x_bar = np.log(0.1)

S0 = 1.0
x0 = x_bar

T_days = 126
Nsteps = T_days

K = 0.0001


# Path simulation (under Q)
def simulate_paths(Npaths, seed=None):
    rng = np.random.default_rng(seed)
    dt = Delta

    S = np.full((Npaths, Nsteps + 1), S0, dtype=float)
    x = np.full((Npaths, Nsteps + 1), x0, dtype=float)
    r = np.zeros((Npaths, Nsteps + 1), dtype=float)

    sqrt_dt = np.sqrt(dt)
    sqrt_one_minus_rho2 = np.sqrt(1.0 - rho**2)

    for n in range(Nsteps):
        eps_S = rng.normal(size=Npaths)
        eps_v = rng.normal(size=Npaths)

        v_t = np.exp(x[:, n])

        # Price of stock risk at time t
        eta_S_t = ((mu_S - rF) / np.sqrt(v_t)
                   + gamma_x * sqrt_dt * (rho * eps_S + sqrt_one_minus_rho2 * eps_v))

        # Update x
        x[:, n+1] = (x[:, n]
                     - theta * (x[:, n] - x_bar) * dt
                     - (rho * eta_S_t + sqrt_one_minus_rho2 * eta_x) * gamma_x * sqrt_dt
                     + gamma_x * sqrt_dt * (rho * eps_S + sqrt_one_minus_rho2 * eps_v))

        # Update S
        S[:, n+1] = S[:, n] * np.exp((rF - 0.5 * v_t) * dt + np.sqrt(v_t * dt) * eps_S)
        r[:, n+1] = np.log(S[:, n+1] / S[:, n])

    # 10-day moving average of squared log-returns
    RV = np.zeros_like(S)
    window = 10
    for t in range(window, Nsteps + 1):
        sq = r[:, t-window+1:t+1]**2
        RV[:, t] = sq.mean(axis=1)

    return S, x, RV


def compute_payoff(RV):
    return np.maximum(RV - K, 0.0)


# LSMC training
def lsmc_train(Npaths_train=100000, seed=123):
    S, x, RV = simulate_paths(Npaths_train, seed=seed)
    dt = Delta
    disc = np.exp(-rF * dt)

    Npaths, T1 = RV.shape
    Payoff = compute_payoff(RV)

    # CF[i, n] = optimal (discounted) value from time n for path i
    CF = np.zeros_like(Payoff)
    CF[:, -1] = Payoff[:, -1]

    # regression coefficients for basis 1, x, RV, x^2, x*RV, RV^2
    beta = np.zeros((T1, 6))

    # exercise only when 10-day RV is defined
    start = 10

    for n in range(T1 - 2, start - 1, -1):
        itm = Payoff[:, n] > 0

        if not np.any(itm):
            CF[:, n] = disc * CF[:, n+1]
            continue

        X_state = x[itm, n]
        RV_state = RV[itm, n]

        # basis functions: 1, x, RV, x^2, x*RV, RV^2
        Xmat = np.column_stack([
            np.ones_like(X_state),
            X_state,
            RV_state,
            X_state**2,
            X_state * RV_state,
            RV_state**2
        ])

        # Y = discounted future cash-flow
        Y = disc * CF[itm, n+1]

        reg = LinearRegression(fit_intercept=False)
        reg.fit(Xmat, Y)
        beta[n, :] = reg.coef_

        C = reg.predict(Xmat)
        payoff_n = Payoff[itm, n]

        exercise_itm = payoff_n > C
        itm_indices = np.where(itm)[0]
        ex_paths = itm_indices[exercise_itm]

        no_ex = np.setdiff1d(np.arange(Npaths), ex_paths)

        CF[ex_paths, n] = payoff_n[exercise_itm]
        CF[no_ex, n] = disc * CF[no_ex, n+1]

    # for times before RV is defined, everyone holds
    for n in range(start - 1, -1, -1):
        CF[:, n] = disc * CF[:, n+1]

    price0 = CF[:, 0].mean()
    return beta, price0, (S, x, RV, CF)



# Exercise boundary
def exercise_boundary(beta, t_index, x_samples=None):
    b = beta[t_index, :]
    if x_samples is None:
        x_samples = np.linspace(x_bar - 1.0, x_bar + 1.0, 50)

    x_vals = []
    rv_vals = []

    b0, b1, b2, b3, b4, b5 = b

    for x_val in x_samples:
        A = b5
        B = b2 + b4 * x_val - 1.0
        C = b0 + b1 * x_val + b3 * x_val**2 + K

        disc = B*B - 4*A*C
        if disc < 0:
            continue

        sqrt_disc = np.sqrt(disc)

        if abs(A) < 1e-12:
            if abs(B) < 1e-12:
                continue
            rv = -C / B
            if rv > K and np.isfinite(rv):
                x_vals.append(x_val)
                rv_vals.append(rv)
            continue

        rv1 = (-B + sqrt_disc) / (2*A)
        rv2 = (-B - sqrt_disc) / (2*A)
        candidates = [rv for rv in (rv1, rv2) if rv > K and np.isfinite(rv)]
        if not candidates:
            continue

        rv_star = min(candidates)
        x_vals.append(x_val)
        rv_vals.append(rv_star)

    return np.array(x_vals), np.array(rv_vals)


