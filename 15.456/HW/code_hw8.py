def lsmc_value_with_policy(beta, Npaths_eval=10000, seed=999):
    S, x, RV = simulate_paths(Npaths_eval, seed=seed)
    Payoff = compute_payoff(RV)

    dt = Delta
    disc = np.exp(-rF * dt)

    Npaths, T1 = RV.shape
    CF = np.zeros_like(Payoff)
    CF[:, -1] = Payoff[:, -1]

    start = 10

    for n in range(T1 - 2, start - 1, -1):
        b = beta[n, :]
        Xn = x[:, n]
        RVn = RV[:, n]

        Xmat_all = np.column_stack([
            np.ones_like(Xn),
            Xn,
            RVn,
            Xn**2,
            Xn * RVn,
            RVn**2
        ])
        C_all = Xmat_all @ b

        payoff_n = Payoff[:, n]
        itm = payoff_n > 0

        ex = np.where(itm & (payoff_n > C_all))[0]
        no_ex = np.setdiff1d(np.arange(Npaths), ex)

        CF[ex, n] = payoff_n[ex]
        CF[no_ex, n] = disc * CF[no_ex, n+1]

    for n in range(start - 1, -1, -1):
        CF[:, n] = disc * CF[:, n+1]

    price0 = CF[:, 0].mean()
    std0 = CF[:, 0].std(ddof=1)
    ci_half = 1.96 * std0 / np.sqrt(Npaths_eval)
    return price0, (price0 - ci_half, price0 + ci_half), (S, x, RV)



