def simulate_once(theta_opt, rho_sim, N_paths=10000, seed=0):
    """
    Compare DP-optimal strategy vs uniform strategy (sell 1/T each period),
    for a given rho_sim.
    """
    rng = np.random.default_rng(seed)

    profits_opt = np.zeros(N_paths)
    profits_uni = np.zeros(N_paths)

    x_uniform = X0 / T

    for n in range(N_paths):
        S_prev_opt = 1.0
        S_prev_uni = 1.0
        W_opt = X0
        W_uni = X0

        pi_opt = 0.0
        pi_uni = 0.0

        for t in range(T):
            # Optimal strategy
            x_t_opt = get_optimal_theta(theta_opt, t, S_prev_opt, W_opt)
            eps = rng.normal(0.0, sigma)
            S_t_opt = S_BAR + rho_sim * (S_prev_opt - S_BAR) + eps
            exec_price_opt = S_t_opt - lam * x_t_opt
            pi_opt += x_t_opt * exec_price_opt
            W_opt -= x_t_opt
            if W_opt < 0:
                W_opt = 0.0
            S_prev_opt = S_t_opt

            # Uniform strategy
            x_t_uni = min(x_uniform, W_uni)
            eps2 = rng.normal(0.0, sigma)
            S_t_uni = S_BAR + rho_sim * (S_prev_uni - S_BAR) + eps2
            exec_price_uni = S_t_uni - lam * x_t_uni
            pi_uni += x_t_uni * exec_price_uni
            W_uni -= x_t_uni
            if W_uni < 0:
                W_uni = 0.0
            S_prev_uni = S_t_uni

        profits_opt[n] = pi_opt
        profits_uni[n] = pi_uni

    return profits_opt.mean(), profits_uni.mean()


