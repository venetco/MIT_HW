from math import log, sqrt, exp

def bs_call_price(S, K, r, sigma, T):
    if T <= 0:
        return max(S - K, 0.0)
    d1 = (log(S / K) + (r + 0.5 * sigma**2) * T) / (sigma * sqrt(T))
    d2 = d1 - sigma * sqrt(T)
    return S * norm.cdf(d1) - K * exp(-r * T) * norm.cdf(d2)

sigma_ref = 0.25 # "true" vol in GBM
C_bs_nocost = bs_call_price(S0, K, r, sigma_ref, T)
print("Frictionless BS call price (σ = 0.25):", C_bs_nocost)


def implied_vol_call(target_price, S, K, r, T, sigma_low=1e-4, sigma_high=3.0, tol=1e-6, max_iter=100):
    """Bisection implied vol for a European call."""
    for _ in range(max_iter):
        mid = 0.5 * (sigma_low + sigma_high)
        price_mid = bs_call_price(S, K, r, mid, T)
        if abs(price_mid - target_price) < tol:
            return mid
        if price_mid > target_price:
            sigma_high = mid
        else:
            sigma_low = mid
    return 0.5 * (sigma_low + sigma_high)

