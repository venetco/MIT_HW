def solve_dp_for_gamma(gamma):
    """
    Re-run the DP for a given risk-aversion gamma.
    Returns (H, policy).
    Uses all global objects: S_tree, theta_grid, etc.
    """
    # precompute alpha_n
    alpha = np.array([R**(N-n) for n in range(N+1)])

    H = [None]*(N+1)
    policy = [None]*(N+1)

    # terminal condition
    H_N = np.zeros((N+1, N_theta))
    for j in range(N+1):
        S_T = S_tree[N, j]
        payoff = max(S_T - K, 0.0)
        for i, theta in enumerate(theta_grid):
            L = liquidation_value(S_T, theta, lam)
            H_N[j, i] = np.exp(-gamma * (L - payoff))
    H[N] = H_N
    policy[N] = np.zeros((N+1, N_theta), dtype=int)

    # backward induction (MINIMIZE val)
    for n in range(N-1, -1, -1):
        H_n = np.zeros((n+1, N_theta))
        pol_n = np.zeros((n+1, N_theta), dtype=int)
        alpha_n = alpha[n]
        H_next = H[n+1]

        for j in range(n+1):
            S = S_tree[n, j]
            for i, theta in enumerate(theta_grid):
                best_val = np.inf
                best_k = 0
                for k, theta_new in enumerate(theta_grid):
                    TC = transaction_cost(S, theta, theta_new, lam)
                    H_up = H_next[j+1, k]
                    H_dn = H_next[j,   k]
                    cont = p*H_up + (1-p)*H_dn
                    val = np.exp(gamma * alpha_n * TC) * cont
                    if val < best_val:
                        best_val = val
                        best_k = k
                H_n[j, i] = best_val
                pol_n[j, i] = best_k

        H[n] = H_n
        policy[n] = pol_n

    return H, policy


