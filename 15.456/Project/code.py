def simulate_optimal_hedge(M, W0, policy, theta_grid):
    tracking_errors = np.zeros(M)
    intervals_all = []

    for m in range(M):
        W = W0
        theta_idx = theta0_index
        theta = theta_grid[theta_idx]
        j = 0
        last_trade_time = 0

        for n in range(N):
            S = S_tree[n, j]

            # optimal new position given (n, node j, current theta_idx)
            k_star = policy[n][j, theta_idx]
            theta_new = theta_grid[k_star]

            # record a trade and its time gap
            if k_star != theta_idx:
                intervals_all.append(n - last_trade_time)
                last_trade_time = n

            # transact and pay/receive TC
            TC = transaction_cost(S, theta, theta_new, lam)
            W -= TC

            # cash accrues at risk-free rate to next time step
            W *= R

            # update state
            theta_idx = k_star
            theta = theta_new

            # stock move (risk-neutral)
            if rng.random() < p:
                j += 1

        # at maturity: liquidate and compute tracking error
        S_T = S_tree[N, j]
        L = liquidation_value(S_T, theta, lam)
        W_T = W + L
        payoff = max(S_T - K, 0.0)
        tracking_errors[m] = W_T - payoff

    return tracking_errors, np.array(intervals_all)


