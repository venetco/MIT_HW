def transaction_cost(S, theta_old, theta_new, lam):
    delta = theta_new - theta_old
    if delta > 0:   # buy
        return (1 + lam) * S * delta
    elif delta < 0: # sell
        return (1 - lam) * S * delta
    else:
        return 0.0

def liquidation_value(S, theta, lam):
    if theta > 0:   # sell long
        return (1 - lam) * S * theta
    elif theta < 0: # buy back short
        return (1 + lam) * S * theta
    else:
        return 0.0


