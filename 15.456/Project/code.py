def simulate_heuristic_delta(M, W0, rebalance_every_steps):
    """
    Heuristic strategy: rebalance to BSM delta every 'rebalance_every_steps' time steps.
    Returns array of tracking errors of length M.
    """
    tracking_errors = np.zeros(M)

    for m in range(M):
        W = W0
        theta = 0.0
        j = 0

        for n in range(N):
            S = S_tree[n, j]

            # re-hedge only at fixed intervals (including time 0)
            if n % rebalance_every_steps == 0:
                tau = T - n*dt
                delta = bs_delta_call(S, K, tau, r, sigma)
                theta_new = delta

                TC = transaction_cost(S, theta, theta_new, lam)
                W -= TC
                theta = theta_new

            # risk-free growth of cash until next step
            W *= R

            # stock move (binomial under Q)
            if rng.random() < p:
                j += 1

        # at maturity: liquidate and compute tracking error
        S_T = S_tree[N, j]
        L = liquidation_value(S_T, theta, lam)
        W_T = W + L
        payoff = max(S_T - K, 0.0)
        tracking_errors[m] = W_T - payoff

    return tracking_errors


